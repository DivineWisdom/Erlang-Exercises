==-module(student).
-export([getstudent/0, part1/0, part2/0]).

getstudent() ->
	[{'Kevin', $M, 19}, {'Benji', $M, 22}, {'Carl', $M, 23}, {'Vegas', $M, 23}, {'Nema', $F, 19}].

part1() ->
	Student = getstudent(),
	[{Name} || {Name, Gender, Age} <- Student, Gender =:= $M, Age > 20].

part2() ->
	Student = getstudent(),
	[{Name} || {Name, Gender, Age} <- Student, Gender =:= $M orelse Age > 18].


